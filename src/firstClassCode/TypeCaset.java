package firstClassCode;

public class TypeCaset {

	public static void main(String[] args) {
		
//		surjeet101@gmail.com
		
//		Widening Casting
		int integervalue=10;
		
		float floatvalue=integervalue;
		
		System.out.println(integervalue +"  "+floatvalue);
		
		
//		Narrowing Casting
		float floatvalue1=10.12f;
		
		int intvalue=(int)floatvalue1;		
		
		System.out.println(floatvalue1 + " "+intvalue );

	}

}
